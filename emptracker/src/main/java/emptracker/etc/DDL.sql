# 사원관리 (EmpTracker;사원 관리 시스템) -- DDL
# 데이터베이스 준비 : db 서버의 여러개 테이블들을 저장할 수 있는 공간 
DROP DATABASE IF EXISTS EmpTracker; -- 동일한 DB명이 존재하면 삭제
CREATE DATABASE EmpTracker;         -- 지정한 DB명으로 데이터베이스 생성
USE EmpTracker;                     -- 지정한 DB를 활성화/사용

CREATE TABLE employees (
    id INT AUTO_INCREMENT PRIMARY KEY,                                          -- 사원 ID (자동 증가, 기본 키)
    name VARCHAR(100) NOT NULL,                                                 -- 사원 이름 (필수 입력)
    phone VARCHAR(20),                                                          -- 사원의 연락처 (선택 입력)
    position VARCHAR(50),                                                       -- 사원의 직급 (예: 팀장, 대리, 사원)
    hire_date DATE,                                                             -- 사원의 입사일 (YYYY-MM-DD 형식)
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,                             -- 데이터 생성 시간 (자동 입력)
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP  -- 데이터 수정 시간 (자동 업데이트)
);

# 사원 등록 시스템에서 사용할 SQL CRUD 데이터 삽입 테스트
# (employees 테이블을 기반으로 작성됨)

# ✅ 1. 데이터 삽입 (Create)

INSERT INTO employees (name, phone, position, hire_date)
VALUES ('김철수', '010-1234-5678', '개발자', '2025-02-20');
# ✅ 2. 데이터 조회 (Read)
# 🔹 모든 사원 조회
SELECT * FROM employees;
# ✅ 2. 데이터 조회 (Read)

# 🔹 특정 사원 조회
SELECT * FROM employees WHERE id = 123; -- 특정 ID의 사원 조회

# ✅ 3. 데이터 수정 (Update)
UPDATE employees
SET name = '이영희', phone = '010-9876-5432', position = '팀장', hire_date = '2024-12-10'
WHERE id = 123;

# ✅ 4. 데이터 삭제 (Delete)
DELETE FROM employees
WHERE id = 123; -- 특정 ID의 사원 조회 후 삭제

# ✅ 5. 페이징 기능이 포함된 조회
SELECT * FROM employees
ORDER BY hire_date DESC
LIMIT 10 OFFSET 0; -- 최신 입사자 기준 10명 조회



